package IfStatement;

public class SelectionStatement {

	public static void main(String[] args) {
		int x = 20;
		if (x < 10) {
		System.out.print("a");
		}
		else if (x > 40) {
			System.out.println("b");
		}
		else if (x < 0) {
			System.out.println("c");
		}
		else { System.out.println ("d");
	}
	}
}
